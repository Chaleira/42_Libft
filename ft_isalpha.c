#include <unistd.h>

void	ft_isalpha(char a)
{
	if (a >= 65 && a <= 90)
		write(1, "1\n", 2);
	else if (a >= 97 && a <= 122)
		write(1, "1\n", 2);
	else
		write(1, "0\n", 2);
}
