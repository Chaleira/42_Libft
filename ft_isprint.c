#include <unistd.h>

void	ft_isprint(char a)
{
	if (a >= 32 && a <= 127)
		write(1, "1\n", 2);
	else if (a >= 97 && a <= 122)
		write(1, "1\n", 2);
	else
		write(1, "0\n", 2);
}
