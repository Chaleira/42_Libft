#include <unistd.h>

void	ft_isalnum(char a)
{
	if (a >= 65 && a <= 90)
		write(1, "1\n", 2);
	else if (a >= 97 && a <= 122)
		write(1, "1\n", 2);
	else if (a >= 48 && a <= 57)
		write(1, "1\n", 2);
	else
		write(1, "0\n", 2);
}

int	main(void)
{
	ft_isalnum('.');
	return (0);
}
